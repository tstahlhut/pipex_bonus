#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)

{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	if (!d && !s)
		return (0);
	if (src < dest)
	{
		while (n--)
			d[n] = s[n];
	}
	else
		while (n--)
			*d++ = *s++;
	return (dest);
}
