#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)

{
	size_t			s;
	unsigned int	i;

	i = 0;
	s = ft_strlen(src);
	if (size < 1)
		return (s);
	while ((src[i] != '\0') && (i < size - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (s);
}
