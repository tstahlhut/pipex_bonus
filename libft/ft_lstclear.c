#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	while (*lst != NULL)
	{
		del((*lst)->content);
		temp = (*lst)->next;
		free(*(lst));
		*lst = temp;
	}
}
